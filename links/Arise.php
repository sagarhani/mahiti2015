	  	  <p>"Thousands of candles can be lighted from a single candle and the life of candle will not be shortened.  Happiness never decreases when it is shared" said lord Buddha.
With a hope to be the candle that lights up every place it goes, parivartan strives to be the change everybody wants to see.
What started out as a humble group of students visiting a rural school to teach a group of young minds the basics of computers, Parivartan has today progressed into a full fledged perennial team of Genesis. 
Blood donation camp, eye donation camp, health checkup for the underprivileged, teaching kids computers, felicitating the toppers from government schools- you name it, Parivartan has done it all.  Bob Marley asked the world to open its eyes, to look within, to see if people are satisfied with their lives. Parivartan decided to do a little something to make the lives of the less fortunate better. It has donated clothes, groceries, books and stationery. Parivartan aims at bringing smiles on the faces of every one it meets, hoping to leave an ever lasting impression. 
For this semester, parivartan has plans that are bigger and better than before. An inter departmental dance competition is on the cards as a fundraiser for the Joy of Giving Week. Joy of Giving is a week in the month of October dedicated to making the world a better place, to change the dynamics of the unprivileged.
Distribution of blankets to the homeless, establishment of a library in a government school, a cook off whose proceeds will go to charity are some of the events planned for the Joy of Giving week.
With the undying love and support it has always received from the Department Of ISE, parivartan is ebullient and is looking forward to what this year brings.

